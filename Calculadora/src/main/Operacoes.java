public class Operacoes {
    
    public float soma (float num1, float num2){
        float res = num1 + num2;
        return res;
    }
    
    public float subtr (float num1, float num2){
        float res = num1 - num2;
        return res;
    }
    
    public float multli (float num1, float num2){
        float res = num1 * num2;
        return res;
    }
    
    public float div (float num1, float num2){
        float res = num1 / num2;
        return res;
    }
    
}
